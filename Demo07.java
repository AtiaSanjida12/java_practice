public class Demo07 {
    public static void main(String[] args){
        //Relational operators --> ==,!=,<,>,<=,>=
        int a=12;
        int b=10;
        boolean c=(a==b);
        boolean d=(a!=b);
        boolean e=(a<b);
        boolean f=(a>b);
        boolean g=(a<=b);
        boolean h=(a>=b);
        System.out.println(c+","+d+","+e+","+f+","+g+","+h);
    }
}

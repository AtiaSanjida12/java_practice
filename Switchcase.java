public class Switchcase {
    public static void main(String[] args){
        //switch statement
        int i=6;
        switch(i){
            case 1:
            System.out.println("i is 1");
            break;
            case 2:
             System.out.println("i is 2");
             break;
             case 3:
              System.out.println("i is 3");
              break;
              default:
                 System.out.println("i>3");
                 break;
        }
        }
        }
    

